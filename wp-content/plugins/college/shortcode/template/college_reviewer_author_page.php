<script>
    jQuery(document).ready(function(){
        var conditions = [];
        var values = "<?php echo $authorIssuePaperReviewer['radiovalues'];?>";
        var res = values.split(",");
        if(res.length){
            jQuery('input:radio').each(function () {
                var rname = jQuery(this).attr('name');
                if (jQuery.inArray(rname, conditions) == -1) conditions.push(rname);
            });
            jQuery.each(conditions, function (i, name) {
                var check = res[i];
                jQuery('input[name="' + name + '"][value="' + check + '"]').attr('checked', true);
            });
        }
        
    });

    jQuery(document).on('click', '#author_paper', function(e){
        e.preventDefault();
        jQuery('#errorMsg').addClass('hide-error');
        var noerrorFlag = 1;
        if(jQuery('#status').val()==0){
            noerrorFlag=0;
        }
        var conditions = [];
        var values = [];
        jQuery('input:radio').each(function () {
            var rname = jQuery(this).attr('name');
            if (jQuery.inArray(rname, conditions) == -1) conditions.push(rname);
        });

        //do validation for each group
        jQuery.each(conditions, function (i, name) {
            values.push(jQuery('input[name="' + name + '"]:checked').val());
            if (jQuery('input[name="' + name + '"]:checked').length == 0) {
                noerrorFlag=0;
            }
        });

        if(!jQuery('#comment').val()){
            noerrorFlag=0;
        }
        if(!jQuery('#strength').val()){
            noerrorFlag=0;
        }
        if(!jQuery('#weekness').val()){
            noerrorFlag=0;
        }
        
        if(noerrorFlag){
            jQuery('#loader-overlay').show();
            var fd = new FormData();
        
            fd.append("paper_id", <?php echo $_GET['id'];?>); 
            fd.append("comment", jQuery('#comment').val());
            fd.append("strength", jQuery('#strength').val());
            fd.append("weekness", jQuery('#weekness').val());
            fd.append("values", values);
            fd.append("status", jQuery('#status').val());            
            fd.append('action', 'college_author_paper_reviewer'); 

            jQuery.ajax({
                type: 'POST',
                url: "<?php echo admin_url('admin-ajax.php'); ?>",
                data: fd,
                contentType: false,
                processData: false,
                success: function(response){
                    window.location = "<?php echo get_buzz_url('college_author_paper_list');?>";
                }
            });
        }
        else{
            jQuery('#errorMsg').removeClass('hide-error');
        }
    });
</script>