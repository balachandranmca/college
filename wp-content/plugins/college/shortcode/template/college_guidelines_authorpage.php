    <h2>Guidelines to <span class="color">Author</span></h2>
    <div class="border"></div>
</div>
<!-- /section title -->

<div class="container">
    <div class="guidelines-content">
        <p>The International Journal of Research in Engineering, Science and Technologies (IJRESTs) is a monthly online International peer reviewed E- journal published in English. This journal publishes only the selected, original research papers, articles and review papers. Authors are requested to submit the manuscripts and not submitted for publication elsewhere. All the authors required to follow the following guidelines for manuscript preparation.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Instructions at a Glance</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Preparation of Manuscripts</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>All manuscripts should be typed in two columns on A-4 size and text must be prepared in MS Word 2007. The paper should be restricted to a maximum of 10 typed pages including figures, photographs, tables, etc.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Format of Manuscripts</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>All manuscripts should be prepared as follows.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Text</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Entire text can be split up into Title, abstract, key-words, introduction, material and methods, observations, results & discussion, conclusion, acknowledgements and symbols, abbreviations, references. Only standard international units should be used.</p>
    </div>
</div>
<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Title</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Should be specific and informative to the contents and concise – Arial 20 font, capitalize each and centre alignment.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Authors</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>12 font, Italic, Bold.</p>
    </div>
</div>
<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Address</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>10 font – normal. Correspondence address with country name in capitals, E-mail ID of each author.</p>
    </div>
</div>
<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Abstract</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Should be in brief with salient features and important conclusion, not exceeding 250 words. No references should be quoted in the abstract.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Key Words</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Below the abstract mention not more than seven keywords related to the contents of the manuscript.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Introduction</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Should be brief and specific. No sub headings in the introduction part. Avoid elaboration of details of earlier works. If the references are used indicate them with number in [ ].</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Literature Review</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Should contain the information collected from various sources of publications including the journals, proceedings, text books, news articles, web sites, thesis, etc. It should be brief and only important points of the work are to be reported. Wherever any kind of publications used , should be cited the same with sequential number in [ ].</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Material and Methods</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Should contain details of the equipment/machinery/chemicals/questionnaire/etc used followed by the description/procedure of work carried out. Any kind of standard procedure/equations/ work/documents used, cite the same as reference in [ ].</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Results and Discussion</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Present the views/report in appropriate and precise format using the important results/observations/readings/data obtained during the studies and represent in a tabular form, graphical form, photographs, charts, etc as may be required for better quality and limited to minimum. Avoid repeated discussion of the same.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Tables</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Use proper heading and numbered as cited in text as Table 1, 2 etc. In case the table is collected from any published source, indicate the same as “source [ ]” and list in references.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Figures</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>All diagrams, graphs, photographs, charts must be represented using proper title and numbered as cited in text as Fig.1, 2, etc. All the figures should be clear and good in quality. Legends must be visible clearly. In case the Figure/s are collected from any published source, indicate the same as “source [ ]” and list in references.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Conclusion</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Conclusion should be the outcome of the paper. Do not repeat the information provided in the abstract as the conclusion. Conclusion/s may comprise the probable scope to continue the work. No tables, figures, references are included.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Acknowledgements</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>Authors may acknowledge the superiors/colleagues/funding agencies/organizations/etc for providing the facilities or extending the support. It should be very short and meaningful.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>References</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>References used to collect the information for preparing this article only to be listed. References should be arranged in an order with serial number in the order as they appeared in the article.</p>
    </div>
</div>

<!-- /Sub Heading -->
<div class="container sub-heading">
    <div class="row">
        <div class="col-md-12 header">
            <h4>Citation of references should be as follows</h4>
        </div>
    </div>
</div>
<!-- /Sub Heading -->

<div class="container">
    <div class="guidelines-content">
        <p>In the text name of the authors followed by year of publication (e.g. Raja M and Mohan S, 2014). If authors are more than two, then should be referred to first author name followed by et al (Raja M, et al 2015). However, references list should include the names of all the authors, full title of the article, abbreviated title of the journal, volume no., issue no., Page nos. </p>
    </div>
</div>

